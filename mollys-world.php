        <p>The overall aim and objective of the Molly's World project was to work closely with the client, Richard Weston (<a id="page-link" href="http://www.richardwestonstudio.com/registry">Richard Weston Studios</a>), to design and produce a prototype web based application for children, age ranges 6 - 11. The prototype would then be used as a demonstration model to secure funding for a much more in depth and expanded application. The brief was that the application should be an experience to learn about nature and the natural world that immerses children in an interactive enviroment, through vivid graphics, animations and audio effects making the learning experience fun and appealing. It also had to work as smoothly as possible on a range of platforms.</p>

